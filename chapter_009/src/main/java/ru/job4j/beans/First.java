package ru.job4j.beans;

/**
 * @author Yury Matskevich
 */
public class First {
	private int id;
	private String name;
	private String age;

	public First() {
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	public String getAge() {
		return age;
	}

	public void setAge(String age) {
		this.age = age;
	}
}
